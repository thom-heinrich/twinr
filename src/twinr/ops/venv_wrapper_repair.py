# BUG-1: Repair modern activation-script variants (`export VIRTUAL_ENV=...`) and
# case-insensitive `Activate.*` helpers instead of only legacy `VIRTUAL_ENV=...`.
# BUG-2: Repair versioned Python entrypoints and distlib `/bin/sh` exec wrappers,
# not only plain `.../.venv/bin/python` shebangs.
# BUG-3: Derive the expected venv root from `expected_interpreter` so activation
# repairs stay aligned with the caller's stable deployment path.
# SEC-1: Replace direct in-place writes with nofollow reads plus atomic same-dir
# replacement to avoid local symlink clobber races and torn writes.
# IMP-1: Preserve interpreter variants and interpreter flags when possible
# (`python3.13`, `pypy3`, `-X ...`) instead of flattening to `bin/python`.
# IMP-2: Make rewrites durable (`fsync`) and cover bash/zsh, csh, fish, and
# PowerShell activation helpers generated by current Python tooling.

"""Repair stale venv entrypoint references inside preserved Pi virtualenvs.

The Pi runtime deploy preserves ``/twinr/.venv`` across repo mirrors so
runtime-only state and installed dependencies survive normal code rollouts.
Historically moved/copied venvs can still carry console-script wrappers or
activation scripts whose absolute interpreter / ``VIRTUAL_ENV`` references
still point at an older checkout such as ``/home/thh/twinr/.venv``. That
silently sends shell-driven commands back into the wrong environment even when
the package itself is installed correctly in the current venv.

This module intentionally stays stdlib-only so it can run early in deploy
flows on Raspberry Pi systems without bootstrapping extra dependencies.
"""

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
import contextlib
import errno
import os
import re
import shlex
import stat
import tempfile


@dataclass(frozen=True, slots=True)
class PiVenvScriptRepairResult:
    """Summarize one venv wrapper / activation repair pass."""

    checked_files: int
    rewritten_files: int
    sample_paths: tuple[str, ...]


_PYTHON_EXECUTABLE_NAME_RE = re.compile(
    r"^(?:python(?:[0-9.]+)?|pypy(?:[0-9.]+)?)$",
    re.IGNORECASE,
)

_ACTIVATE_VIRTUAL_ENV_PATTERNS = (
    # POSIX shell activators:
    #   VIRTUAL_ENV="/path/.venv"
    #   export VIRTUAL_ENV='/path/.venv'
    re.compile(
        r'^(?P<prefix>\s*(?:export\s+)?VIRTUAL_ENV\s*=\s*)(?P<quote>["\']?)(?P<path>/[^"\r\n\']*?/.venv)(?P=quote)(?P<suffix>\s*)$',
        re.MULTILINE,
    ),
    # csh/tcsh activators:
    #   setenv VIRTUAL_ENV /path/.venv
    re.compile(
        r'^(?P<prefix>\s*setenv\s+VIRTUAL_ENV\s+)(?P<quote>["\']?)(?P<path>/[^"\r\n\']*?/.venv)(?P=quote)(?P<suffix>\s*)$',
        re.MULTILINE,
    ),
    # fish activators:
    #   set -gx VIRTUAL_ENV /path/.venv
    re.compile(
        r'^(?P<prefix>\s*set\s+-gx\s+VIRTUAL_ENV\s+)(?P<quote>["\']?)(?P<path>/[^"\r\n\']*?/.venv)(?P=quote)(?P<suffix>\s*)$',
        re.MULTILINE,
    ),
    # PowerShell activators:
    #   $env:VIRTUAL_ENV = "/path/.venv"
    re.compile(
        r'^(?P<prefix>\s*\$env:VIRTUAL_ENV\s*=\s*)(?P<quote>["\']?)(?P<path>/[^"\r\n\']*?/.venv)(?P=quote)(?P<suffix>\s*)$',
        re.MULTILINE | re.IGNORECASE,
    ),
)


def repair_venv_python_shebangs(
    *,
    bin_dir: Path,
    expected_interpreter: str,
    sample_limit: int = 12,
) -> PiVenvScriptRepairResult:
    """Rewrite relocated venv entrypoint references to the in-place interpreter.

    Virtual environments record absolute interpreter paths in generated console
    scripts and absolute environment roots in activation helpers. If a venv was
    historically copied from another checkout root, package imports may still
    work via ``/twinr/.venv/bin/python -m ...`` while direct wrapper execution
    or ``. /twinr/.venv/bin/activate`` jumps back into the old venv.

    Repairs performed here:
    * direct shebang wrappers such as ``#!/old/.venv/bin/python3.13 -X utf8``
    * distlib safe wrappers using ``#!/bin/sh`` + ``'''exec' ... "$0" "$@"``
    * activation scripts that assign ``VIRTUAL_ENV`` across common shells
    """

    expected = str(expected_interpreter).strip()
    if not expected:
        raise ValueError("expected_interpreter must not be empty")
    if sample_limit <= 0:
        raise ValueError("sample_limit must be greater than zero")
    if not bin_dir.exists():
        return PiVenvScriptRepairResult(
            checked_files=0,
            rewritten_files=0,
            sample_paths=(),
        )
    if not bin_dir.is_dir():
        raise NotADirectoryError(str(bin_dir))

    # Do not resolve away a stable deploy symlink here: callers pass the path
    # they want wrappers to contain, so activation helpers should be aligned to
    # that exact venv root.
    expected_path = Path(expected)
    expected_venv_dir = str(expected_path.parent.parent)

    checked_files = 0
    rewritten_files = 0
    sample_paths: list[str] = []

    for candidate in _iter_regular_files(bin_dir):
        if _looks_like_activation_script(candidate):
            checked_files += 1
            if _repair_activation_script(
                candidate=candidate,
                expected_venv_dir=expected_venv_dir,
            ):
                rewritten_files += 1
                if len(sample_paths) < sample_limit:
                    sample_paths.append(candidate.name)
            continue

        try:
            original, metadata = _read_file_bytes_nofollow(candidate)
        except OSError as exc:
            if _should_skip_candidate_error(exc):
                continue
            raise

        first_line, _, _ = original.partition(b"\n")
        if not first_line.startswith(b"#!") or b"\0" in first_line:
            continue

        checked_files += 1

        rewritten = _rewrite_simple_shebang(
            original=original,
            expected_interpreter=expected,
        )
        if rewritten is None:
            rewritten = _rewrite_distlib_safe_wrapper(
                original=original,
                expected_interpreter=expected,
            )
        if rewritten is None or rewritten == original:
            continue

        _atomic_replace_bytes(candidate=candidate, data=rewritten, metadata=metadata)
        rewritten_files += 1
        if len(sample_paths) < sample_limit:
            sample_paths.append(candidate.name)

    return PiVenvScriptRepairResult(
        checked_files=checked_files,
        rewritten_files=rewritten_files,
        sample_paths=tuple(sample_paths),
    )


def _should_skip_candidate_error(exc: BaseException) -> bool:
    """Return whether one file-level error is safe to treat as a vanished entry."""

    return isinstance(exc, OSError) and exc.errno in {
        errno.ENOENT,
        errno.ELOOP,
    }


def _iter_regular_files(bin_dir: Path):
    """Yield regular files from one bin directory in deterministic name order."""

    entries: list[tuple[str, Path]] = []
    with os.scandir(bin_dir) as scandir_it:
        for entry in scandir_it:
            try:
                metadata = entry.stat(follow_symlinks=False)
            except FileNotFoundError:
                continue
            if not stat.S_ISREG(metadata.st_mode):
                continue
            entries.append((entry.name, Path(entry.path)))
    for _, path in sorted(entries, key=lambda item: item[0]):
        yield path


def _read_file_bytes_nofollow(candidate: Path) -> tuple[bytes, os.stat_result]:
    """Read one regular file without following symlinks when the OS supports it."""

    flags = os.O_RDONLY
    if hasattr(os, "O_NOFOLLOW"):
        flags |= os.O_NOFOLLOW

    fd = os.open(candidate, flags)
    try:
        metadata = os.fstat(fd)
        if not stat.S_ISREG(metadata.st_mode):
            raise OSError(errno.EINVAL, "not a regular file", str(candidate))
        chunks: list[bytes] = []
        while True:
            chunk = os.read(fd, 1024 * 1024)
            if not chunk:
                break
            chunks.append(chunk)
        return b"".join(chunks), metadata
    finally:
        os.close(fd)


def _atomic_replace_bytes(*, candidate: Path, data: bytes, metadata: os.stat_result) -> None:
    """Replace one file atomically in-place while preserving mode / ownership."""

    fd, tmp_name = tempfile.mkstemp(
        dir=str(candidate.parent),
        prefix=f".{candidate.name}.tmp-",
        suffix=".rewrite",
    )
    tmp_path = Path(tmp_name)
    try:
        with os.fdopen(fd, "wb") as tmp_file:
            tmp_file.write(data)
            tmp_file.flush()
            os.fchmod(tmp_file.fileno(), stat.S_IMODE(metadata.st_mode))
            with contextlib.suppress(PermissionError, AttributeError, NotImplementedError):
                os.fchown(tmp_file.fileno(), metadata.st_uid, metadata.st_gid)
            os.fsync(tmp_file.fileno())
        os.replace(tmp_path, candidate)
        _best_effort_fsync_dir(candidate.parent)
    except Exception:
        with contextlib.suppress(FileNotFoundError):
            tmp_path.unlink()
        raise


def _best_effort_fsync_dir(path: Path) -> None:
    """Best-effort fsync of one directory for rename durability on POSIX."""

    if not hasattr(os, "O_DIRECTORY"):
        return

    flags = os.O_RDONLY | getattr(os, "O_DIRECTORY", 0)
    fd: int | None = None
    try:
        fd = os.open(path, flags)
        os.fsync(fd)
    except OSError as exc:
        if exc.errno not in {
            errno.EBADF,
            errno.EINVAL,
            errno.ENOTSUP,
            errno.EPERM,
            errno.EROFS,
        }:
            raise
    finally:
        if fd is not None:
            os.close(fd)


def _looks_like_activation_script(candidate: Path) -> bool:
    """Return whether one venv bin entry is an activation helper."""

    lower_name = candidate.name.lower()
    return lower_name == "activate" or lower_name.startswith("activate.")


def _repair_activation_script(*, candidate: Path, expected_venv_dir: str) -> bool:
    """Rewrite stale ``VIRTUAL_ENV`` assignments inside one activation helper."""

    try:
        original_bytes, metadata = _read_file_bytes_nofollow(candidate)
    except OSError as exc:
        if _should_skip_candidate_error(exc):
            return False
        raise

    try:
        original_text = original_bytes.decode("utf-8")
    except UnicodeDecodeError:
        return False

    repaired_text = _rewrite_activation_virtual_env(
        text=original_text,
        expected_venv_dir=expected_venv_dir,
    )
    if repaired_text == original_text:
        return False

    _atomic_replace_bytes(
        candidate=candidate,
        data=repaired_text.encode("utf-8"),
        metadata=metadata,
    )
    return True


def _rewrite_activation_virtual_env(*, text: str, expected_venv_dir: str) -> str:
    """Normalize stale activation-script venv roots to the current venv path."""

    repaired = text
    expected = str(expected_venv_dir).strip()
    for pattern in _ACTIVATE_VIRTUAL_ENV_PATTERNS:
        repaired = pattern.sub(
            lambda match: _replace_activation_match(
                match=match,
                expected_venv_dir=expected,
            ),
            repaired,
        )
    return repaired


def _replace_activation_match(*, match: re.Match[str], expected_venv_dir: str) -> str:
    """Return one activation line with the expected venv root injected."""

    current = str(match.group("path") or "").strip()
    if current == expected_venv_dir or "/.venv" not in current:
        return match.group(0)

    prefix = match.group("prefix")
    quote = match.group("quote")
    suffix = match.group("suffix")
    return f"{prefix}{quote}{expected_venv_dir}{quote}{suffix}"


def _rewrite_simple_shebang(*, original: bytes, expected_interpreter: str) -> bytes | None:
    """Repair one direct Python shebang if it still targets an old venv."""

    first_line, separator, remainder = original.partition(b"\n")
    if not first_line.startswith(b"#!") or b"\0" in first_line:
        return None

    interpreter = _shebang_interpreter(first_line)
    if not interpreter or not _looks_like_venv_python(interpreter):
        return None

    replacement = _replacement_interpreter(
        current_interpreter=interpreter,
        expected_interpreter=expected_interpreter,
    )
    if replacement == interpreter:
        return None

    try:
        shebang_text = first_line.decode("utf-8")
    except UnicodeDecodeError:
        return None

    payload = shebang_text[2:].rstrip("\r")
    parts = payload.split(maxsplit=1)
    if not parts:
        return None
    suffix = ""
    if len(parts) == 2:
        suffix = " " + parts[1]

    line_suffix = "\r" if shebang_text.endswith("\r") else ""
    rewritten = f"#!{replacement}{suffix}{line_suffix}".encode("utf-8")
    return rewritten + (separator + remainder if separator else b"")


def _rewrite_distlib_safe_wrapper(*, original: bytes, expected_interpreter: str) -> bytes | None:
    """Repair one distlib safe-wrapper script using ``#!/bin/sh`` + ``exec``."""

    lines = original.splitlines(keepends=True)
    if len(lines) < 2:
        return None

    first_line = lines[0]
    if _shebang_interpreter(first_line) != "/bin/sh":
        return None

    try:
        second_line_text = lines[1].decode("utf-8")
    except UnicodeDecodeError:
        return None

    try:
        tokens = shlex.split(second_line_text, posix=True)
    except ValueError:
        return None

    if len(tokens) < 3 or tokens[0] != "exec" or tokens[-2:] != ["$0", "$@"]:
        return None

    interpreter = tokens[1]
    if not _looks_like_venv_python(interpreter):
        return None

    replacement = _replacement_interpreter(
        current_interpreter=interpreter,
        expected_interpreter=expected_interpreter,
    )
    if replacement == interpreter:
        return None

    rewritten_second_line = second_line_text.replace(interpreter, replacement, 1)
    if rewritten_second_line == second_line_text:
        return None

    lines[1] = rewritten_second_line.encode("utf-8")
    return b"".join(lines)


def _shebang_interpreter(first_line: bytes) -> str:
    """Extract the interpreter path from one script shebang line."""

    try:
        text = first_line.decode("utf-8")
    except UnicodeDecodeError:
        return ""
    if not text.startswith("#!"):
        return ""
    payload = text[2:].strip()
    if not payload:
        return ""
    return payload.split(maxsplit=1)[0]


def _looks_like_venv_python(interpreter: str) -> bool:
    """Return whether one path points at a venv-local Python executable."""

    normalized = str(interpreter or "").strip()
    if "/.venv/bin/" not in normalized:
        return False
    return _looks_like_python_executable_name(Path(normalized).name)


def _looks_like_python_executable_name(name: str) -> bool:
    """Return whether one executable basename is Python-like."""

    return bool(_PYTHON_EXECUTABLE_NAME_RE.fullmatch((name or "").strip()))


def _replacement_interpreter(*, current_interpreter: str, expected_interpreter: str) -> str:
    """Choose the best in-place interpreter path for one repaired wrapper.

    Prefer preserving versioned interpreter basenames such as ``python3.13`` if
    that executable exists in the target venv. Otherwise, fall back to the
    caller-provided interpreter path.
    """

    expected_path = Path(expected_interpreter)
    current_name = Path(current_interpreter).name
    if current_name == expected_path.name:
        return str(expected_path)
    if not _looks_like_python_executable_name(current_name):
        return str(expected_path)

    candidate = expected_path.parent / current_name
    if candidate.exists():
        return str(candidate)
    return str(expected_path)


